// konstanta adalah nilai yang sudah ditentukan dan bisa dirubah
public class Konstanta {
    public static void main (String [] args){
        int topi = 1000;
        int baju = 5000;
        int hasil = topi + baju;
        int kali = topi * baju;
        int bagi  = baju - topi;

        System.out.println (hasil);
        System.out.println (kali);
        System.out.println (bagi);
        
    }
}