
public class Variabel
{
    public static void main(String[] args)
    {
        int x = 9;
        int y = 3;
        int z = 30;

        //Operator
        // 1. penjumlahan

        int hasiltambah = x + y;
        System.out.println (hasiltambah);

        // 2. pengurangan

        int hasilkurang = x-z;
        System.out.println (hasilkurang);

        //3. pembagian

        int hasilbagi = x/y;
        System.out.println (hasilbagi);

        //4. persen

        int modulus= z % 3;
        System.out.println(modulus);
    }
}